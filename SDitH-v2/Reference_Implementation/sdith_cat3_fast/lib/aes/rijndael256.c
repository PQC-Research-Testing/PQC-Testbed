static const size_t __SR2[Nb] = {2, 14, 26, 6, 18, 30, 10, 22};

static const uint8_t __rc[14] = {0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36, 0x6c, 0xd8, 0xab, 0x4d};

static inline void _SubBytes(rijndael256_state_t state)
{
  for (size_t i = 0; i < Nbytes; i++)
  {
    state[i] = __sbox[state[i]];
  }
}

static inline void _ShiftRows(rijndael256_state_t state)
{
  // shift row 1 by 1
  for (size_t i = 0; i < Nb-1; i++)
  {
    state[4*i+1] ^= state[4*i+5];
    state[4*i+5] ^= state[4*i+1];
    state[4*i+1] ^= state[4*i+5];
  }

  // shift row 2 by 3
  for (size_t i = 0; i < Nb-1; i++)
  {
    state[__SR2[i+0]] ^= state[__SR2[i+1]];
    state[__SR2[i+1]] ^= state[__SR2[i+0]];
    state[__SR2[i+0]] ^= state[__SR2[i+1]];
  }

  // shift row 3 by 4
  for (size_t i = 0; i < Nb/2; i++)
  {
    state[4*i+ 3] ^= state[4*i+19];
    state[4*i+19] ^= state[4*i+ 3];
    state[4*i+ 3] ^= state[4*i+19];
  }
}

static inline void _MixColumns(rijndael256_state_t state)
{
  for (size_t i = 0; i < Nb; i++)
  {
    uint8_t tmp[4] = {
      state[4*i+0],
      state[4*i+1],
      state[4*i+2],
      state[4*i+3]
    };

    state[4*i+0] = __mul2[tmp[0]] ^ __mul3[tmp[1]] ^ tmp[2] ^ tmp[3];
    state[4*i+1] = __mul2[tmp[1]] ^ __mul3[tmp[2]] ^ tmp[3] ^ tmp[0];
    state[4*i+2] = __mul2[tmp[2]] ^ __mul3[tmp[3]] ^ tmp[0] ^ tmp[1];
    state[4*i+3] = __mul2[tmp[3]] ^ __mul3[tmp[0]] ^ tmp[1] ^ tmp[2];
  }
}

static inline void _AddRoundKey(rijndael256_state_t state, const rijndael256_key_t key)
{
  for (size_t i = 0; i < Nbytes; i++)
  {
    state[i] ^= key[i];
  }
}

static void _round(rijndael256_state_t state, const rijndael256_key_t key)
{
  _SubBytes(state);
  _ShiftRows(state);
  _MixColumns(state);
  _AddRoundKey(state, key);
}

static void _keyschedule(rijndael256_key_t key, const rijndael256_key_t oldkey, int round)
{
  for (size_t j = 0; j < Nk; j++)
  {
    if (j == 0)
    {
      key[0] = oldkey[0] ^ __sbox[oldkey[29]] ^ __rc[round-1];
      key[1] = oldkey[1] ^ __sbox[oldkey[30]];
      key[2] = oldkey[2] ^ __sbox[oldkey[31]];
      key[3] = oldkey[3] ^ __sbox[oldkey[28]];
    } else if (j == 4) {
      key[16] = oldkey[16] ^ __sbox[key[12]];
      key[17] = oldkey[17] ^ __sbox[key[13]];
      key[18] = oldkey[18] ^ __sbox[key[14]];
      key[19] = oldkey[19] ^ __sbox[key[15]];
    } else {
      for (size_t i = 0; i < 4; i++)
      {
        key[j*4+i] = oldkey[j*4+i] ^ key[(j-1)*4+i];
      }
    }
  }
}

static void _encrypt(rijndael256_state_t res, const rijndael256_rk_t *roundkeys)
{  
  _AddRoundKey(res, roundkeys->rk[0]);
  for (size_t i = 1; i < Nr; i++)
  {
    _round(res, roundkeys->rk[i]);
  }
  _SubBytes(res);
  _ShiftRows(res);
  _AddRoundKey(res, roundkeys->rk[Nr]);
}

static void _increment_ctr(uint8_t res[32], const uint8_t ctr[32])
{
  uint64_t tmp = ctr[0] | (((uint32_t)ctr[1])<<8) | (((uint32_t)ctr[2])<<16) | (((uint32_t)ctr[3])<<24);
  tmp += 1;
  res[0] = (tmp >>  0) & 0xFF;
  res[1] = (tmp >>  8) & 0xFF;
  res[2] = (tmp >> 16) & 0xFF;
  res[3] = (tmp >> 24) & 0xFF;
  for (size_t i = 1; i < 8; i++)
  {
    tmp >>= 32;
    tmp += ctr[4*i] | (((uint32_t)ctr[4*i+1])<<8) | (((uint32_t)ctr[4*i+2])<<16) | (((uint32_t)ctr[4*i+3])<<24);
    res[4*i+0] = (tmp >>  0) & 0xFF;
    res[4*i+1] = (tmp >>  8) & 0xFF;
    res[4*i+2] = (tmp >> 16) & 0xFF;
    res[4*i+3] = (tmp >> 24) & 0xFF;
  }
}


void rijndael256_key_schedule_ref(rijndael256_rk_t *roundkeys, const uint8_t key[32])
{
  // expand key
  memcpy(roundkeys->rk[0], key, Nbytes);
  for (size_t i = 1; i <= Nr; i++)
  {
    _keyschedule(roundkeys->rk[i], roundkeys->rk[i-1], i);
  }
}



void rijndael256_ecb_encrypt_1block_ref(uint8_t res[ 32], const uint8_t x[ 32], const rijndael256_rk_t *roundkeys)
{
  memcpy(res, x, 32);
  // encrypt
  _encrypt(res, roundkeys);
}

void rijndael256_ecb_encrypt_2blocks_ref(uint8_t res[ 64], const uint8_t x[ 64], const rijndael256_rk_t *roundkeys)
{
  memcpy(res, x, 64);
  // encrypt
  _encrypt(res, roundkeys);
  _encrypt(res+32, roundkeys);
}

void rijndael256_ecb_encrypt_4blocks_ref(uint8_t res[128], const uint8_t x[128], const rijndael256_rk_t *roundkeys)
{
  memcpy(res, x, 128);
  // encrypt
  _encrypt(res, roundkeys);
  _encrypt(res+32, roundkeys);
  _encrypt(res+64, roundkeys);
  _encrypt(res+96, roundkeys);
}

void rijndael256_ctr_encrypt_2blocks_ref(uint8_t res[64], const uint8_t ctr[32], const uint8_t key[32]) {
  // expand key
  rijndael256_rk_t roundkeys;
  rijndael256_key_schedule_ref(&roundkeys, key);
  
  // encrypt
  memcpy(res, ctr, 32);
  _encrypt(res, &roundkeys);
  _increment_ctr(res+32, ctr);
  //_increment_ctr(ctr, res+32); // this function shall not update ctr
  _encrypt(res+32, &roundkeys);
}
